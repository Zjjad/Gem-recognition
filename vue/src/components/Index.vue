    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>菱锰矿是一种碳酸盐矿物，其理论化学组成为碳酸锰，并可能含有Fe、Ca、Zn、Mg等元素。这些元素的存在往往取代了矿物中的其他成分。菱锰矿的英文名称“Rhodochrosite”源自希腊词“rhodon”和“chrosis”，分别指玫瑰（Rose）和颜色（Color），形象地描述了它特殊的玫瑰红色。菱锰矿的色泽常呈淡粉色，有时也呈褐色或灰色，氧化后会变为褐黑色，具有玻璃光泽。它的摩氏硬度在3.5~4之间，且性脆，遇盐酸会起泡。\n\n菱锰矿晶体形态多样，可以是六方晶系或三方晶系的菱形体，也可呈现为柱状、针状、纤维状等。在沉积成因下，菱锰矿常形成隐晶质，呈块状、结核状、肾状、钟乳状、土状集合体。在热液成因下，它多呈显晶质，以粒状或柱状集合体的形式出现。\n\n菱锰矿最早发现于阿根廷安第斯山脉，因此有“阿根廷石”的别称。产自阿根廷的菱锰矿基本呈钟乳石状，外表光滑完整，晶体透亮，横切面会呈现红白相间的花纹，非常美丽，被誉为“印加玫瑰”或“红纹石”，在古印加帝国，它被视为圣石，并在西方矿物晶体收藏界备受推崇。\n\n除了具有较高的观赏价值，菱锰矿还是提取锰和制造锰合金、锰化合物的矿物原料。在珠宝领域，色泽艳丽、透明的菱锰矿可制作为饰品，其中晶体较大的可当成宝石；颗粒细小、半透明的菱锰矿集合体可成为玉雕材料。\n\n总的来说，菱锰矿因其美丽的颜色和特殊的物理性质，在宝石和矿物收藏领域都有着重要的地位</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>堇青石，英文名称为Cordierite，为硅酸盐矿物。它的颜色主要呈浅蓝色或浅紫色，具有玻璃光泽，且能量相当稳定，不能以加热的方式来改变其颜色，是一种货真价实的宝石。当铁元素含量大于镁元素时，它被称为铁堇青石。而堇青石中镁含量高于铁含量时，则直接称为堇青石。其中，产于印度的富镁品种因其出色的质量而常被用来做成宝石，也被称为印度石。\n\n堇青石具有明显的多色性，用肉眼从不同角度观察，可以发现其颜色有明显的变化，这也是堇青石区别于其他相似宝石的重要特征之一。此外，堇青石还有另一个古老的名字——Dichroite，意为“两种颜色的石头”，也源自其多色性特征。\n\n堇青石硬度为7～7.5，密度约为2.61g/cm³，不算很重。其化学式为Mg2Al4Si5O18，可含有Na、K、Ca、Fe、Mn等元素及H2O。堇青石在矿物学分类上属于斜方晶系，具有一组完全解理。它的光泽为玻璃光泽，透明度为透明至半透明。\n\n在地质成因上，堇青石是由含铝量较高的岩石经过中度到高度热力变质作用所形成，主要产在片麻岩或含铝量较高的片岩中。而宝石级的堇青石则主要赋存于富镁的蚀变火山岩中。\n\n此外，堇青石还有一个美丽的别称——“水蓝宝石”，这是因为它的颜色近似于蓝宝石，且价格相对亲民，因此被戏称为“穷人家的蓝宝石”。然而，尽管堇青石的硬度较高，但其韧性一般，比较脆，因此在佩戴或保存时需要注意避免撞击。\n\n总的来说，堇青石以其独特的颜色、稳定的能量和明显的多色性在宝石界占有一席之地，同时也是矿物学和地质学研究的重要对象。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>桂榴石，也被称为铁钙铝榴石，是石榴石矿物的一种。它属于钙铝榴石的变种，具有铁和钙的成分。桂榴石的内部常见热浪效应，这是因为其中常见大量磷灰石晶体，它们能形成一种独特的漩涡状结构。其颜色通常为褐黄色、酒黄色，因此也有“肉桂石”的别称。\n\n桂榴石是半透明的，具有浅红褐色，有时被人们当作次等宝石。在宝石学中，它与芬达石（锰铝榴石）同为橙色系石榴石的重要品种，尽管颜色相似，但两者可通过折射率和内部包裹体进行区分。例如，桂榴石的折射率为1.740，而芬达石的折射率为1.810。此外，桂榴石内部常见锆石、大量圆形磷灰石和方解石的晶体，它们呈现出“油状”或“漩涡状”的包裹体，被称为热浪效应或糖浆状包裹体。\n\n总的来说，桂榴石以其独特的颜色、内部结构和宝石学特征在石榴石家族中占据一席之地。</p></div>
