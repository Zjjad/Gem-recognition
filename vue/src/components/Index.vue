    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>变石，也被称为亚历山大石，是一种具有变色效应的金绿宝石，在矿物学中属于金绿宝石的一种。它之所以会神奇地变色，是由于含有铬元素，这种金属元素也是红宝石、祖母绿、翡翠产生美妙颜色的原因。变石的主要成分为BeAl2O4，含有微量的铬元素，导致它对绿光的透射能力最强，红光次之，对其它颜色的光线则全部吸收。因此，变石在日光或日光灯的照射下呈现出的是以绿色调为主的颜色，而在白炽灯或烛光灯的照射下则呈现出以红色调为主的颜色，于是人们就给了变石“白昼里的祖母绿，黑夜里的红宝石”的美誉。\n\n变石在自然界中主要以短柱状和板状的斜方晶体形式出现，其折光率为1.745~1.754，密度为3.73g/cm³，硬度高，韧性极好。在自然光下，变石主要透过绿光，表现为绿色变石；在钨丝白炽灯、蜡烛、油灯等照射下，能透射更多红光，呈现为红色变石。变石在不少国家被定为六月诞生石，象征健康、富贵、长寿，因此也有健康之石的称呼。它还是代表双子座的星座石，价值高，受到多数人的喜爱。\n\n变石的大小、透明度、瑕疵多少也会影响其价格，粒大、透明度高而瑕疵少的变石少而名贵。变石的主要产地包括俄罗斯的乌拉尔山脉、斯里兰卡、津巴布韦和巴西等，其中俄罗斯产出的变石质量最为上乘，但资源几乎已经枯竭。在珠宝市场上，品质一般的变石价格也在小几万元一克拉。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>珍珠是一种古老的有机宝石，主要产于珍珠贝类和珠母贝类软体动物体内，由于这些动物的内分泌作用而生成含碳酸钙的矿物（文石）珠粒。这些珠粒是由大量微小的文石晶体集合而成，因此具有瑰丽色彩和高雅气质。\n\n珍珠的形状和颜色各异，多数不透明，最常见的形状是圆形和梨形，颜色通常为白色或浅色，且具有不同程度的光泽。根据地质学和考古学的研究，珍珠在地球上已经存在了两亿年之久。在中国，珍珠的药用历史也已经有两千多年。\n\n珍珠的形成过程充满了神奇与偶然。当沙子甚至寄生虫被急流冲进珠蚌内时，珠蚌为了减轻刺激，会分泌出一层层被称为珍珠母或珍珠质的微小晶体，这些晶体使得珍珠具有独特的彩虹般的光泽。珠蚌的存活期大约为30年，但形成一颗豌豆大小的珍珠可能需要长达十年的时间。珍珠的质地纯净、光泽优雅，因此被誉为“珠宝皇后”，自古以来就是王室女性搭配礼服和正装的首选。\n\n在国际宝石界，珍珠被视为六月生辰的幸运石，同时也是结婚十三周年和三十周年的纪念石。它象征着健康、纯洁、富有和幸福，是人们表达爱意和祝福的珍贵礼物。\n\n此外，珍珠也以其丰富的文化内涵和艺术价值为人们所珍视。在古代，珍珠被用作饰品和药材，具有多种功效。在现代，珍珠不仅被广泛用于珠宝制作，还成为艺术设计和时尚界的灵感来源。\n\n总的来说，珍珠是一种独特而珍贵的宝石，它的美丽和神秘吸引了无数人的目光。无论是作为珠宝饰品还是文化符号，珍珠都以其独特的魅力在人们的生活中发挥着重要作用。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>锆石，英文名称Zircon，是一种硅酸盐矿物，化学式为ZrSiO₄。它是地球上形成最古老的矿物之一，广泛存在于酸性火成岩，也产于变质岩和其他沉积物中。锆石过去还被叫作锆英石，日本称之为“风信子石”，它是十二月生辰石，象征成功。\n\n锆石具有很高的折光率和较强的色散，无色或淡蓝色的品种加工后，像钻石一样有较强的出火现象。由于它在外观上与钻石很相似，因而被誉为可与钻石媲美的宝石。不过，锆石的硬度比钻石低，并且密度也小于钻石，这是区分两者的重要特征。\n\n锆石有多种颜色，如黑、白、橙、褐、绿或无色透明等，经过切割后的宝石级锆石很像是钻石。其中，无色和蓝色是最受欢迎的颜色。无色锆石可以是天然的，也可以通过热处理黄色、褐色或红色锆石得到。蓝色锆石是所有锆石中价值最高的，要求颜色鲜艳纯正，透明度髙，内部洁净，切割完美。\n\n锆石的化学性质稳定，因而在河流的砂砾中也可以见到宝石级的锆石。锆石按其结晶程度可分为高型、中型、低型三个品种，折射率、密度、硬度值从低型到高型逐渐变大。锆石切割时，要求刻面宝石的台面垂直光轴，以使人们在台面方向观察时看不到后刻面重影及多色性。\n\n锆石含有Hf、Th、U、TR等混入物，提炼后可以制得金属锆，也可用作硅酸盐玻璃添加剂。锆石的工业用途很广，但也存在放射性，因此在选购和佩戴时需要谨慎。\n\n总的来说，锆石以其独特的美感和多样的颜色受到人们的喜爱，同时在工业领域也有着广泛的应用。然而，由于其可能存在的放射性，人们在选购和佩戴时需要注意安全</p></div>
