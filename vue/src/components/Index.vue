    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>萤石，也被称为氟石，是一种在自然界中较为常见的矿物。其主要成分是氟化钙（CaF2），并可能含有其他杂质元素，如Y、Ce等稀土元素，以及少量的Fe2O3、SiO2等。这些杂质元素的存在使得萤石的颜色丰富多变，常显鲜艳的颜色，且硬度较低，比小刀还要软。\n\n萤石的结晶形态为八面体和立方体，晶体呈玻璃光泽，质脆，莫氏硬度为4，熔点为1360℃。此外，萤石还具有完全解理的性质。部分萤石样本在受到摩擦、加热或紫外线照射时，可以发光，这也是它得名的一个重要原因。\n\n在工业上，萤石是氟的主要来源，用于提取制备氟元素及其各种化合物。其中，氢氟酸的生产就大量使用了萤石。氢氟酸是通过酸级萤石（氟石精矿）同硫酸在加热炉或罐中反应而产生的，它被广泛应用于玻璃、陶瓷、水泥等建材工业中。此外，萤石在炼钢、铝生产等工业过程中也作为重要的熔剂使用。\n\n同时，萤石的颜色鲜艳且结晶形态美观，因此在收藏、装饰和雕刻工艺品方面也有很高的价值。然而，需要注意的是，大多数萤石含有放射性物质，对人体可能造成伤害，因此在使用和收藏时需要谨慎。\n\n总的来说，萤石是一种在工业和装饰领域都有广泛应用的重要矿物，但同时也需要注意其可能带来的潜在风险。如需更多关于萤石的信息，建议查阅相关地质学或矿物学书籍。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>铁铝榴石（Almandine）是一种天然磨料矿物，其名称源自小亚细亚的阿拉班达（Alabanda），这是古代切割和加工石榴子石的地方。铁铝榴石主要成分为Fe3Al2(SiO4)3，其中铁的成分较多，导致其呈现出带褐色调的暗红色，并常常透明。由于其自身的化学成分，在日常光线下，铁铝榴石常呈暗红色和棕红色。\n\n铁铝榴石的比重为3.85~4.20g/cm³，硬度为7~7.5，具有玻璃光泽或油脂光泽，透明至半透明，具脆性，含相当多的针状包裹体。它的晶系为等轴晶系，晶体近圆粒状，常见异常干涉色，折光率为1.76~1.82，密度为3.8~4.2g/cm³。铁铝榴石的包裹体通常表现为针状金红石包裹体，且排列得较为稀疏。如果排列密集，就可能出现四射星光。\n\n铁铝榴石在宝石界中非常常见，通常被称为贵榴石，是质量达到宝石级的紫红色铁铝石榴子石。由于其熔点高、硬度大、边角锋利、自锐性好等特点，铁铝榴石被广泛用于制作微细粒磨粉，特别适合用于对电子工业的半导体、荧光屏，光学工业的镜头、镜片的研磨。同时，它也是喷砂抛光、制磨纸浆砂轮和高精度砂轮、砂布的理想材料，对于印刷板、玻璃制品、皮革、骨料、石料、塑料、电镀层的加工也有良好的效果。\n\n铁铝榴石主要产于变质岩、花岗岩和一些火成岩中。此外，由于其特殊的化学成分和物理性质，铁铝榴石在古埃及的遗址中就有发现，并且在希腊罗马时代，这种宝石被人们用来驱邪护身。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>绿柱石是一种铍-铝硅酸盐矿物，其化学式为Be3Al2(SiO3)6。这种矿物的主要成分包括氧化铍（BeO）14.1%，氧化铝（Al2O3）19%，以及氧化硅（SiO2）66.9%。它属于六方晶系，晶体常呈六方柱状，柱面有纵纹，晶体可能非常小，但也可能长达数米。硬度为7.5~8，比重为2.63~2.80。纯净的绿柱石是无色的，甚至可以是透明的，但大部分为绿色，也有浅蓝色、黄色、白色和玫瑰色的，具有玻璃光泽。\n\n绿柱石在矿物学上属绿柱石族，它的变种颜色丰富多样，有淡蓝色的海蓝宝石，深绿色的祖母绿，金黄色的金绿柱石，以及粉红色的铯绿柱石等。这些变种因颜色鲜艳、光泽美丽而备受人们喜爱，尤其是祖母绿和海蓝宝石，是珍贵的宝石品种。\n\n绿柱石的主要产地位于南美的哥伦比亚、巴西，非洲的赞比亚、埃塞俄比亚、马达加斯加，亚洲的乌拉尔山和中国的西北等地。它是炼铍的主要矿物原料，色泽美丽者则是珍贵的宝石。</p></div>
