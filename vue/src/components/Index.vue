    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>锂紫玉是一种首饰用有色宝石，其历史可以追溯到1902年，当时加利福尼亚州圣迭戈县发现了一种尚未确认的粉红色晶体标本，后被送到矿物学家George Frederick Kunz手中。Kunz确认该晶体实际上是锂辉石，但因其先前不为人知的颜色，这一新发现的晶体成为了锂辉石矿物的一个新的类别。锂紫玉（kunzite）的名字也是以Kunz的名字命名的。\n\n锂紫玉在宝石界中以其丰富的颜色范围而著称，从细腻柔和的粉红色到浓郁的紫罗兰紫色。其中，最有价值的色彩是浓郁的粉红色至蓝紫色，但最常见的是浅粉色到浅紫罗兰色。锂紫玉的净度也是一个重要的考量因素，如果不含任何内含物，其价格会更高。\n\n从物理性质来看，锂紫玉的硬度为6.5至7，这意味着它相对坚硬，但仍需小心处理以避免碎裂。由于其具有两个完整的劈裂方向，锂紫玉在切磨和镶嵌过程中需要格外小心。此外，锂紫玉并不强韧，重击或极端压力、温度变化都可能导致其开裂或碎裂。因此，为了降低碎裂风险，珠宝商通常会将锂紫玉宝石镶在防护性的镶座里，或用于制作不容易磨损的首饰，如吊坠和胸针。\n\n处理方面，锂紫玉可以通过辐射并加热来提升其颜色，但处理过的颜色可能会因长时间暴露在热或强光下而褪色。清洁时，应避免使用蒸汽或超音波清洗，最好采用温肥皂水清洗。\n\n总的来说，锂紫玉是一种独特且珍贵的宝石，其丰富的色彩和相对较高的硬度使其在宝石界占有一席之地。然而，由于其脆性和对热、光的敏感性，收藏者和珠宝商在处理和保养锂紫玉时需格外小心。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>蓝宝石是一种重要的宝石种类，主要成分是氧化铝（Al2O3），属于刚玉宝石中除红宝石（Ruby）之外的其他颜色刚玉宝石的通称。它的硬度达到9，仅次于钻石，是世界上珍贵的五大贵重宝石之一。\n\n蓝宝石的颜色非常丰富，包括蓝色、黄色、绿色、紫色、粉红色、橙色和无色等，其中蓝色的蓝宝石最为常见，是由于其中混有少量钛（Ti）和铁（Fe）杂质所致。蓝宝石的颜色深浅不一，有时在同一颗石中也会有多种颜色。它最大的特点是颜色不均，常见平行六方柱面排列的、深浅不同的平直色带和生长纹。\n\n蓝宝石主要产于暗色玄武岩中，为三方晶系，常见晶体习性为柱状双锥，锥角和柱面上可能呈现条纹，轴面上常见三角形生长纹，解理不发育，韧性极好。天然蓝宝石的常见包裹体包括直的色带、带有应力裂纹的晶体包裹体、金红石包裹体、带晕圈的石墨包裹体、负晶包裹体、羽状愈合裂隙等。\n\n蓝宝石象征着沉着冷静、高贵优雅，也代表着智慧和大度，被古时的人们视为吉祥之物。其产地遍布世界各地，其中比较知名的产地包括斯里兰卡、缅甸、澳大利亚、泰国、印度克什米尔地区等。其中，克什米尔蓝宝石被誉为蓝宝石中的极品，颜色鲜艳且纯正，具有天鹅绒效应，现已停止开采。而山东蓝宝石因内部缺陷少，也属优质蓝宝石。\n\n影响蓝宝石价值的三个主要因素包括颜色、净度和切工。一般来说，颜色越浓郁且不含杂质，宝石越值钱。价值最高的蓝宝石颜色，由蓝色至靛蓝色，色度高越好。其中高品质的蓝宝石颜色称为皇家蓝、矢车菊蓝。\n\n总的来说，蓝宝石以其丰富的色彩、高贵的品质以及深远的文化内涵，深受人们的喜爱和追捧。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>拉长石（Labradorite）是斜长石的一种，属于重要的造岩矿物，广泛出现于各种中、基性和超基性岩中。它是由钠长石（NaAlSi3O8，符号Ab）和钙长石（CaAl2Si2O8，符号An）组成的类质同象系列，通常用An分子的百分含量来表示斜长石的成分，拉长石属中性斜长石，An含量在50~70%之间。\n\n拉长石的颜色非常丰富，包括灰、褐到黑色，而可作宝石的拉长石有红、蓝、绿色的晕彩。拉长石具有特殊的光学效应，其内部的长距锯片双晶或具有音固溶体折离形成的钠长石的微细的交互层，以及有时存在的平行零一零经面的微细孔隙，使得透明或半透明的拉长石品种可以呈现出蓝、绿、黄等晕色，有的具有如欧泊般的美丽效果。\n\n拉长石的晶体可长达2米，产在辉长岩、苏长岩、玄武岩、粒玄岩等岩石中。由于其独特的颜色和光学效应，拉长石在宝石界中备受青睐。其中，蓝色的拉长石最为昂贵，其次是绿色和红色的拉长石。\n\n然而，并非所有的拉长石都可以作为宝石使用，只有少数透明或半透明的拉长石才具有宝石的价值。此外，拉长石也具有一定的寓意和象征意义，被视为吉祥、幸运和健康的象征。\n\n总的来说，拉长石是一种独特的矿物，不仅具有重要的地质学意义，而且在宝石学和文化领域中也有着广泛的应用和深远的影响。</p></div>
