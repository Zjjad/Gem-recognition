    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>坦桑石，也称为黝帘石（Zoisite），是一种硅酸盐矿物，最初被用作装饰材料。自1967年在坦桑尼亚发现了蓝紫色的透明晶体之后，坦桑石在宝石业中的地位日益提高。这种宝石首次在赤道雪山脚下的阿鲁沙地区被发现，随后蒂凡尼（Tiffany）公司将其推向了国际珠宝市场。为了纪念当时新成立的坦桑尼亚共和国，这种宝石被命名为坦桑石（Tanzanite），它在国外还被称为丹泉石。\n\n坦桑石的化学成分为Ca₂Al₃(SiO₄)₃(OH)，属于正交晶系，硬度为摩氏硬度6~7。宝石级黝帘石的产地有坦桑尼亚、格陵兰、奥地利、瑞士等，但只有坦桑尼亚产出的这一颜色的黝帘石才能真正称得上是坦桑石。坦桑石的形成时间约为5.8亿年前，但直到近50年前才被发现。\n\n坦桑石具有多色性，从不同的晶体方向看会有不同的颜色，如蓝色、紫色等，有时在垂直并且有光照的情况下还会呈现红色。坦桑石的颜色在蓝色宝石中接近蓝宝石，但价格相对较低，其多色性和价格优势使得它在蓝色宝石市场中占有一席之地。\n\n坦桑石在古代凯尔特民族中被认为拥有灵力，能够增强佩戴者的能力，并带来灵感和创意。在现代，坦桑石因其产地有限和开采时间相对短暂而具有较大的收藏价值。同时，它美丽的颜色和独特的宝石特征也使其备受人们喜爱。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>绿宝石是一种铍-铝硅酸盐矿物，通常为六方柱形晶体，颜色多变，包括各种绿色，如淡蓝色、金黄色等。绿宝石的定义主要在于其成分和结构，而非仅仅依据颜色。\n\n绿宝石的变种丰富，每种都有其独特的魅力和价值。例如，祖母绿是绿宝石之王，属于四大名贵宝石之一，其绿色美丽动人，具有独特的魅力，是结婚55周年的纪念石。海蓝宝石则属于绿宝石矿物，呈现天蓝色，象征着沉着、勇敢、幸福、长寿，尽管在价值上可能不如祖母绿，但其独特的蓝色也吸引了众多爱好者。此外，还有金绿宝石、铯绿柱石等多种变种，每一种都有其独特的色彩和魅力。\n\n在宝石学中，绿宝石因其鲜艳的颜色和稀有性而具有很高的价值。它的色彩和透明度是决定其价值的主要因素。绿宝石的历史悠久，人们最早发现绿宝石可以追溯到公元前5000年的埃及文化。在古代，绿宝石被用来代表荷尔斯（太阳神）和奥西里斯（死亡和复活神），身佩绿宝石的人被认为是有神的保护。\n\n同时，绿宝石也是一种坚硬且美丽的宝石，常被用来制造珠宝，如项链、戒指、耳环等。然而，由于绿宝石容易受到强光和强热的影响，因此在保存和佩戴时需要注意避免暴露在过强的光线或热源下。\n\n总的来说，绿宝石是一种独特且珍贵的宝石，无论是从历史、文化还是宝石学的角度来看，都具有重要的价值和意义。</p></div>
    <div style="width: 33%;height: 500px;float: left;display:flex; align-items:center; justify-content:center;border:1px solid #000;"><p>赛黄晶是一种含钙的硼硅酸盐矿物，化学式为CaB2[SiO4]2，英文名称为Danburite。其发现地是美国康涅狄格州的丹伯里（Danbury），因而得名。赛黄晶的晶系为斜方晶系，常呈短柱状，顶端楔形，晶面具纵纹，可以形成晶簇，集合体呈块状或粒状。其硬度为7，比重为3，折射率约为1.630~1.636，密度约为3.00g/cm3，具有玻璃光泽和低色散特性（色散值为0.017）。\n\n赛黄晶的颜色丰富多样，包括无色、白色、黄色、褐色以及偶尔可见的粉红色，其中蜜黄色和酒黄色与托帕石相似。在紫外光下，赛黄晶可以发出蓝色荧光，部分还具有猫眼效应。赛黄晶的外观非常美丽，显得华贵、高雅和亮丽，因此深受女性朋友的喜爱，常被制成饰品佩戴在身上，增添自身的气质。\n\n从宝石学的角度来看，能够达到宝石级的透明色赛黄晶原矿非常稀少，而且原矿本身非常小，所以宝石级的赛黄晶一般只能做成戒面和吊坠等小件成品。因此，对于赛黄晶的爱好者来说，赛黄晶更多是一种收藏品而非日常佩戴的饰品。\n\n此外，赛黄晶还有滋养身体和舒缓心情的功效。由于赛黄晶这类天然宝石含有大量的矿物质和微量元素，当人体长时间佩戴时，可以逐渐吸收这些有益的物质。同时，赛黄晶的色泽亮丽、纯净，佩戴它可以让人在视觉上感到愉悦和放松，从而舒缓情绪。\n\n总的来说，赛黄晶是一种具有独特魅力和多重价值的宝石，无论是从矿物学、宝石学还是文化价值的角度来看，都值得人们深入了解和欣赏。</p></div>
